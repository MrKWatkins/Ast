namespace MrKWatkins.Ast;

/// <summary>
/// The <see cref="Message.Level"/> of a <see cref="Message"/>.
/// </summary>
public enum MessageLevel
{
    /// <summary>
    /// An information message.
    /// </summary>
    Info,

    /// <summary>
    /// A warning message.
    /// </summary>
    Warning,

    /// <summary>
    /// An error message.
    /// </summary>
    Error
}